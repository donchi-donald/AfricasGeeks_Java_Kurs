public class Aufgabe3 {

	public static void main(String[] args) {
		int i = 3;
		i++;
		System.out.println(i);//4
		++i; //5
		System.out.println(i);//5
		System.out.println(++i);//6  
		int j = i++;//j=6   i=7
		System.out.println(j);//6
		j = ++i;//8
		System.out.println(j);//8

	}

}
